public class EsercizioDue {
    public static void main(String[] args) {
        int number = 4;

        switch (number) {
            case 0: {
                System.out.println("Il numero e' 0");
                break;
            }
            case 1: {
                System.out.println("Il numero e' 1");
                break;
            }
            case 2: {
                System.out.println("Il numero e' 2");
                break;
            }
            case 3: {
                System.out.println("Il numero e' 3");
                break;
            }
            default: {
                System.out.println("Errore il numero non e' compreso tra 0 e 3");
            }

        }
    }



}
